#!/usr/bin/env node
/**
 * Refresh the local OFAC sanctioned-address list from the official OFAC SDN.
 *
 *   node scripts/update-ofac-addresses.js
 *
 * Fetches OFAC's SDN "advanced" XML, extracts digital-currency addresses, and
 * rewrites src/compliance/ofacSanctionedAddresses.ts. The Chainalysis oracle
 * (ATOMIC_CHAINALYSIS_API_KEY) remains the authoritative live source; this keeps
 * the offline baseline current. Network-fetched data only — nothing is executed.
 */
const fs = require('node:fs');
const path = require('node:path');

const SDN_URL = process.env.OFAC_SDN_URL
  || 'https://sanctionslistservice.ofac.treas.gov/api/download/sdn_advanced.xml';
const OUT = path.join(__dirname, '..', 'src', 'compliance', 'ofacSanctionedAddresses.ts');

// Address shapes we screen. Extend as OFAC adds new chains.
const ADDRESS_PATTERNS = [
  /^0x[a-fA-F0-9]{40}$/,                       // EVM (ETH, etc.)
  /^(bc1|[13])[a-zA-HJ-NP-Z0-9]{25,62}$/,      // Bitcoin
  /^(ltc1|[LM])[a-km-zA-HJ-NP-Z1-9]{26,45}$/,  // Litecoin
  /^T[1-9A-HJ-NP-Za-km-z]{33}$/                // Tron
];

function looksLikeCryptoAddress(value) {
  return ADDRESS_PATTERNS.some((re) => re.test(value));
}

async function main() {
  console.log(`Fetching OFAC SDN: ${SDN_URL}`);
  const res = await fetch(SDN_URL, { headers: { Accept: 'application/xml' }, signal: AbortSignal.timeout(30000) });
  if (!res.ok) throw new Error(`OFAC download failed: HTTP ${res.status}`);
  const xml = await res.text();

  // Digital-currency addresses live in <VersionDetail> values; filter by address shape.
  const details = xml.match(/<VersionDetail[^>]*>([^<]+)<\/VersionDetail>/g) || [];
  const found = new Set();
  for (const tag of details) {
    const value = tag.replace(/<[^>]+>/g, '').trim();
    if (looksLikeCryptoAddress(value)) found.add(value);
  }

  if (found.size === 0) {
    throw new Error('No digital-currency addresses parsed — OFAC format may have changed; not overwriting.');
  }

  const addresses = [...found].sort((a, b) => a.localeCompare(b));
  const header = `// OFAC-sanctioned digital-currency addresses (local baseline).
//
// AUTO-GENERATED by scripts/update-ofac-addresses.js from the official OFAC SDN.
// Last refreshed: ${new Date().toISOString()} · ${addresses.length} addresses.
//
// The Chainalysis sanctions oracle (ATOMIC_CHAINALYSIS_API_KEY) is the authoritative
// live source; this offline list is the deterministic fallback. Matching is
// case-insensitive; addresses are normalized to lowercase at load.
`;
  const body = `export const OFAC_SANCTIONED_ADDRESS_SEED: string[] = [
${addresses.map((a) => `  '${a}'`).join(',\n')}
];

export const OFAC_SANCTIONED_ADDRESSES: Set<string> = new Set(
  OFAC_SANCTIONED_ADDRESS_SEED.map((address) => address.trim().toLowerCase())
);
`;
  fs.writeFileSync(OUT, `${header}\n${body}`);
  console.log(`Wrote ${addresses.length} addresses to ${path.relative(process.cwd(), OUT)}`);
  console.log('Rebuild + redeploy to apply (npm run build; restart).');
}

main().catch((error) => {
  console.error('OFAC refresh failed:', error.message);
  process.exit(1);
});
